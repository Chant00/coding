from typing import List


class Solution:
    def pancakeSort(self, arr: List[int]) -> List[int]:
        """"""
        ans = []
        for i in range(len(arr), 1, -1):
            # å¯»æ‰¾æœ€å¤§ç…é¥¼çš„ç´¢å¼•
            max_idx = 0
            for j in range(i):
                if arr[j] > arr[max_idx]:
                    max_idx = j
            # reverse
            # ç¬¬ä¸€æ¬¡åè½¬ï¼Œå°†æœ€å¤§å€¼æ”¾åˆ°arr[0]
            for k in range((max_idx + 1) // 2):
                arr[k], arr[max_idx - k] = arr[max_idx - k], arr[k]
            # ç¬¬äºŒæ¬¡åè½¬ï¼Œå°†æœ€å¤§å€¼æ”¾åˆ°arr[i]
            for k in range(i // 2):  # æ³¨æ„è¿™é‡Œæ˜¯åè½¬arr[0:i],ä¸æ˜¯[0:len(arr)]
                arr[k], arr[i - 1 - k] = arr[i - 1 - k], arr[k]
            ans.append(max_idx + 1)
            ans.append(i)
        return ans

