from typing import List


class Solution:
    def numberOfArithmeticSlices(self, nums: List[int]) -> int:
        # dp[i]è®°å½•çš„æ˜¯ä»¥nums[i]ç»“å°¾çš„ç­‰å·®æ•°åˆ—ä¸ªæ•°ï¼Œdp[i]=dp[i-1]+1ã€‚
        # å¦‚nums=[1,2,3,4,5]ä»¥nums[3]=4ç»“å°¾çš„æœ‰[[2,3,4],[1,2,3,4]]ï¼Œåˆ™ä»¥nums[4]=5ç»“å°¾çš„æœ‰[[2,3,4,5],[1,2,3,4,5],[3,4,5]]ï¼Œåªå¤šä¸€ä¸ª
        dp = 0
        ans = 0
        for i in range(2, len(nums)):
            if nums[i] - nums[i - 1] == nums[i - 1] - nums[i - 2]:
                dp += 1
                ans += dp
            else:
                dp = 0
        return ans
